const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const campgroundSchema = Schema({
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  reviews: [
    {
      type: Schema.Types.ObjectId,
      ref: "Review",
    },
  ],
});

let Campground = mongoose.model("Campground", campgroundSchema);

module.exports = Campground;
